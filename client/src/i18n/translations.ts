export type Language = 'en' | 'fr'

export const getTranslation = (lang: Language, key: string): string => {
  const keys = key.split('.')
  let value: any = translations[lang]
  for (const k of keys) {
    if (value == null) return key
    value = value[k]
  }
  return typeof value === 'string' ? value : key
}
